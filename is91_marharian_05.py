def radixSort(arr, d):  # d - кількість розрядів
    div = 1 # дільник за допомогою якого визначається розряд для сортування
    while d / div > 0: # перевірка чи має число даний розряд
        countingSort(arr, div) # викликаємо функцію сортування підрахунком
        div*= 10 # для переходу до наступного розряду збільуємо дільник в 10 разів


