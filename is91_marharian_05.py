import random

def countingSort(A, div): # А - вхідний масив, div - дільник
    n = len(A)  # n - довжина вхідного масиву А
    output = [0] * (n) # вихідний масив
    C = [0] * (10) # масив для зберігання кількості входжень цифри

    for i in range(0, n):
        C[int(A[i]/div) % 10] += 1 # C[i] записується кількість елементів, які дорівнюють i

    for i in range(1, 10):
        C[i] += C[i - 1] # в C[i] записується кількість елементів, які не більші за i

    for i in range(n-1, -1,-1):
        output[C[int(A[i]/div) % 10] - 1] = A[i] # розставляємо елементи масива в правильній послідовності
        C[int(A[i]/div) % 10] -= 1 # зменшуємо кількість на 1
    for i in range(0, len(A)):
        A[i] = output[i] # копіюємо в А значення відсортованого масива

